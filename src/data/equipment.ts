// ─── Equipment data types ──────────────────────────────────────────────────────

export type EquipmentCategory =
  | 'Devices'
  | 'Stationery & Literacy'
  | 'Robots & Coding'
  | 'Games & Cognitive'
  | 'AAC & Communication'
  | 'Sensory & Regulation'
  | 'Screens & Classroom Hardware'
  | 'Audio & Hearing'
  | 'Furniture & Environment'
  | 'Wearables & Safety';

export type PriceBand = 'Under £50' | '£50–150' | '£150–500' | '£500+';
export type SupplierType = 'Amazon' | 'UK Specialist' | 'School Reseller' | 'Direct';
export type ReviewStatus = 'Reviewed' | 'Needs Review' | 'In Progress';
export type EqBadge = 'SEND Friendly' | 'UK Specialist' | 'Amazon Available' | 'School Quote' | 'Research Based' | 'Needs Review';
export type EqAudience = 'Students' | 'Parents' | 'Schools' | 'SEND' | 'Teachers';
export type PurchaseModel = 'Buy' | 'Quote' | 'Subscription' | 'Lease';

export interface EquipmentSafetyScore {
  accessibility: number | null;
  durability: number | null;
  sendSuitability: number | null;
  procurementSuitability: number | null;
  setupComplexity: number | null;
  total: number | null;
}

export interface EquipmentProduct {
  id: string;
  slug: string;
  name: string;
  brand: string;
  category: EquipmentCategory;
  subcategory: string;
  audience: EqAudience[];
  educationLevel: string[];
  senCategory: string[];
  bestFor: string;
  priceBand: PriceBand;
  supplierName: string;
  supplierType: SupplierType;
  reviewStatus: ReviewStatus;
  ukFocus: boolean;
  purchaseModel: PurchaseModel;
  /**
   * Legacy direct URL — kept as a fallback so unmapped suppliers still work.
   * **Do not link to this directly from cards.** Use
   * `resolveProductAffiliateUrl(product)` so tracking parameters are applied.
   */
  affiliateLink: string;
  /**
   * Optional override pointing at a key in `supplierLinks` (see
   * src/utils/affiliateLinks.ts). When set, the resolver routes through the
   * registry entry rather than auto-deriving from `supplierName`.
   */
  supplierKey?: string;
  /**
   * Optional preferred route for suppliers that have both a direct store and
   * an Amazon listing (e.g. Apple, Microsoft). Defaults to 'direct' unless the
   * `supplierName` itself contains "Amazon".
   */
  preferredRoute?: 'direct' | 'amazon';
  featured: boolean;
  badges: EqBadge[];
  desc: string;
  safetyScore: EquipmentSafetyScore;
}

export interface EquipmentBundle {
  id: string;
  slug: string;
  name: string;
  tagline: string;
  targetNeed: string;
  audience: EqAudience[];
  productSlugs: string[];
  totalPriceBand: string;
  senCategory: string[];
  desc: string;
}

// ─── Helpers ───────────────────────────────────────────────────────────────────

function makeSlug(name: string): string {
  return name.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '');
}

