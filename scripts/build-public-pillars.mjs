#!/usr/bin/env node
/**
 * build-public-pillars.mjs
 *
 * Generates src/data/publicPillarsData.ts from the reviewed pillar dataset
 * (data/tools_public_pillars_reviewed.csv). Re-run whenever the CSV is updated:
 *
 *   node scripts/build-public-pillars.mjs
 *
 * MAPPING (CSV column → public pillar, 1:1, no averaging, no invention):
 *   pillar_a_data_protection → dataPrivacy
 *   pillar_b_safeguarding    → safeguarding
 *   pillar_c_age_curriculum  → ageSuitability
 *   pillar_d_transparency    → transparency
 *   pillar_e_inclusion       → accessibility
 *
 * pillar_f_governance is the internal 6th pillar — DELIBERATELY EXCLUDED from the
 * public five-pillar model. The published composite is the reviewer's
 * promptly_safety_score_v2 (authoritative; already excludes governance).
 *
 * Rows are keyed by `slug`. Rows whose slug has no matching tool in
 * src/data/tools.ts are still emitted (inert until that tool exists) but logged.
 */
import fs from 'node:fs';

const CSV = 'data/tools_public_pillars_reviewed.csv';
const OUT = 'src/data/publicPillarsData.ts';
const REVIEWER = 'CR';          // reviewer of record (founder decision)
const METHODOLOGY = '2.2';      // frozen methodology version (no leading 'v' — marks add it)

// ── Minimal RFC-4180 CSV parser (handles quoted fields with commas/newlines) ──
function parseCsv(text) {
  const rows = [];
  let row = [], field = '', q = false;
  for (let i = 0; i < text.length; i++) {
    const c = text[i];
    if (q) {
      if (c === '"') { if (text[i + 1] === '"') { field += '"'; i++; } else q = false; }
      else field += c;
    } else if (c === '"') q = true;
    else if (c === ',') { row.push(field); field = ''; }
    else if (c === '\n') { row.push(field); rows.push(row); row = []; field = ''; }
    else if (c === '\r') { /* skip */ }
    else field += c;
  }
  if (field.length || row.length) { row.push(field); rows.push(row); }
  return rows;
}

const MONTHS = ['JAN','FEB','MAR','APR','MAY','JUN','JUL','AUG','SEP','OCT','NOV','DEC'];
function normaliseDate(s) {
  s = (s || '').trim();
  if (!s) return '';
  // "2026-06-01" → "JUN 2026"
  let m = s.match(/^(\d{4})-(\d{2})-\d{2}$/);
  if (m) return `${MONTHS[+m[2] - 1]} ${m[1]}`;
  // "Apr 2026" → "APR 2026"
  m = s.match(/^([A-Za-z]{3})[a-z]*\s+(\d{4})$/);
  if (m) return `${m[1].toUpperCase()} ${m[2]}`;
  return s.toUpperCase();
}

const num = (x) => {
  const n = parseFloat(x);
  return Number.isFinite(n) ? n : null;
};

// ── App tool slugs (for match reporting) ──
const toolsSrc = fs.readFileSync('src/data/tools.ts', 'utf8');
const appSlugs = new Set(
  [...toolsSrc.matchAll(/name:"([^"]+)"/g)].map(([, n]) =>
    n.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, ''),
  ),
);

// ── Parse CSV ──
const grid = parseCsv(fs.readFileSync(CSV, 'utf8'));
const header = grid[0];
const idx = (name) => header.indexOf(name);
const COL = {
  slug: idx('slug'),
  a: idx('pillar_a_data_protection'),
  b: idx('pillar_b_safeguarding'),
  c: idx('pillar_c_age_curriculum'),
  d: idx('pillar_d_transparency'),
  e: idx('pillar_e_inclusion'),
  v2: idx('promptly_safety_score_v2'),
  reviewed: idx('lastReviewed'),
};

const entries = [];
const skipped = [];
const unmatched = [];
for (const r of grid.slice(1)) {
  if (!r.length || r.every((x) => x.trim() === '')) continue;
  const slug = (r[COL.slug] || '').trim();
  const pillars = {
    dataPrivacy: num(r[COL.a]),
    safeguarding: num(r[COL.b]),
    ageSuitability: num(r[COL.c]),
    transparency: num(r[COL.d]),
    accessibility: num(r[COL.e]),
  };
  const composite = num(r[COL.v2]);
  // Never invent or average: a row must have a slug, all five public pillars, and a composite.
  if (!slug || composite == null || Object.values(pillars).some((v) => v == null)) {
    skipped.push(slug || '(no slug)');
    continue;
  }
  if (!appSlugs.has(slug)) unmatched.push(slug);
  entries.push({ slug, pillars, composite, verifiedDate: normaliseDate(r[COL.reviewed]) });
}

// ── Emit TS ──
const body = entries
  .map((e) => {
    const p = e.pillars;
    return `  ${JSON.stringify(e.slug)}: { itemId: ${JSON.stringify(e.slug)}, pillars: { dataPrivacy: ${p.dataPrivacy}, safeguarding: ${p.safeguarding}, ageSuitability: ${p.ageSuitability}, transparency: ${p.transparency}, accessibility: ${p.accessibility} }, composite: ${e.composite}, methodologyVersion: ${JSON.stringify(METHODOLOGY)}, reviewer: ${JSON.stringify(REVIEWER)}, verifiedDate: ${JSON.stringify(e.verifiedDate)}, status: 'published' },`;
  })
  .join('\n');

const out = `// AUTO-GENERATED by scripts/build-public-pillars.mjs — DO NOT EDIT BY HAND.
// Source: ${CSV}. Re-run the script to regenerate after the CSV changes.
//
// Public five-pillar scores only. pillar_f_governance (the internal 6th pillar)
// is excluded; composite is the reviewer's promptly_safety_score_v2.
import type { PublicToolScore } from './publicPillars';

export const PUBLISHED_PUBLIC_SCORES: Record<string, PublicToolScore> = {
${body}
};
`;

fs.writeFileSync(OUT, out);

console.log(`Wrote ${OUT}`);
console.log(`  published rows : ${entries.length}`);
console.log(`  skipped (incomplete/no slug): ${skipped.length}`);
console.log(`  unmatched (no app tool yet) : ${unmatched.length}`);
if (unmatched.length) console.log('    ' + unmatched.join(', '));
